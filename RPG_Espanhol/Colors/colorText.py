# Cores para utilizar ao decorrer do programa

# Cor vermelha
def red(text):
    return f"\033[0;31m{text}\033[m"

# Cor verde
def green(text):
    return f"\033[0;32m{text}\033[m"

# Cor amarela
def yellow(text):
    return f"\033[0;33m{text}\033[m"

# Cor azul
def blue(text):
    return f"\033[0;34m{text}\033[m"

# Cor roxo
def purple(text):
    return f"\033[0;35m{text}\033[m"

# Cor azul claro
def lightBlue(text):
    return f"\033[0;36m{text}\033[m"

# Cor cinza
def gray(text):
    return f"\033[0;37m{text}\033[m"
